/**
 * Creates a Material Symbols icon [ImageVector] from SVG path data.
 *
 * All Material Symbols use a 960×960 viewport rendered at 24dp. This mirrors the pattern used by
 * `material-icons-extended` internally, but parsed from SVG path strings rather than expanded Builder calls. The result
 * is cached via the backing-field pattern for zero repeat-allocation cost.
 */
private fun meshtasticIcon(name: String, pathData: String): ImageVector = ImageVector.Builder(
    name = name,
    defaultWidth = 24.dp,
    defaultHeight = 24.dp,
    viewportWidth = 960f,
    viewportHeight = 960f,
)
    .apply { addPath(pathData = PathParser().parsePathString(pathData).toNodes(), fill = SolidColor(Color.Black)) }
    .build()

